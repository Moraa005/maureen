# Arithmetic Operators -perform simple calculations
x =45
y =21

print(x + y)
print(x - y)
print(x * y)
print(x / y)
print(x % y) #Modulus - returns the reminder

# Comparison operator
a = 56
b = 2
print(a < b)
print(a > b)
print(a <= b)
print(a >= b)
print(a == b) #Equal to
print(a != b) #Not equal to

# Assignment operator
z = 6
print(z)

num = 2
num +=10
print(num)

# Logic operators
num1 = 5
num2 = 10
print(num1 < num2 and num1 == num2)
print(num1 < num2 or num1 == num2)
print(not(num1 < num2 and num1 == num2))
