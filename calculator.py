# Assignment
m = float(input("Enter first number:"))
n = float(input("Enter the second number"))
operator = input("Enter the operator:")
if operator == "+":
    print("Results is:",m+n)
elif operator == "-":
    print("Results is:",m-n)
elif operator == "*":
    print("Results is:",m*n)
elif operator == "/":
    print("Results is:",m/n)
else:
    print("Invalid operator")


